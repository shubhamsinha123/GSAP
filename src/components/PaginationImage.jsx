import * as React from "react";
import ImageList from "@mui/material/ImageList";
import ImageListItem from "@mui/material/ImageListItem";
import ImageListItemBar from "@mui/material/ImageListItemBar";
import ListSubheader from "@mui/material/ListSubheader";
import IconButton from "@mui/material/IconButton";
import InfoIcon from "@mui/icons-material/Info";
import { TablePagination } from "@mui/material";
import styled from "styled-components";
import { useTheme } from "@mui/material/styles";
import useMediaQuery from "@mui/material/useMediaQuery";

export default function TitlebarImageList(props) {
  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(5);
  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };
  const theme = useTheme();
  const isSmallScreen = useMediaQuery(theme.breakpoints.down("sm"));

  const styles = {
    container: {
      display: isSmallScreen ? "flex" : null,
      width: isSmallScreen ? null : "45rem",
      height: "35rem",
      marginLeft: isSmallScreen ? null : "21%",
      flexDirection: isSmallScreen ? "column" : null,
    },
    imageList: {
      width: "200px",
    },
    tableRowNumbers: {
      marginLeft: "20% !important",
    },
  };
  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };
  return (
    <Section>
      <ImageList
        // sx={{ width: 1000, height: 450, marginLeft: 30 }}
        style={styles.container}
      >
        <ImageListItem key="Subheader" cols={2}>
          <ListSubheader component="div">Breakfast</ListSubheader>
        </ImageListItem>
        {itemData.map((item) => (
          // style={styles.imageList}
          <ImageListItem key={item.img}>
            <img
              src={`${item.img}?w=248&fit=crop&auto=format`}
              srcSet={`${item.img}?w=248&fit=crop&auto=format&dpr=2 2x`}
              alt={item.title}
              loading="lazy"
            />
            <ImageListItemBar
              title={item.title}
              subtitle={item.author}
              actionIcon={
                <IconButton
                  sx={{ color: "rgba(255, 255, 255, 0.54)" }}
                  aria-label={`info about ${item.title}`}
                >
                  <InfoIcon />
                </IconButton>
              }
            />
          </ImageListItem>
        ))}
      </ImageList>
      <TablePagination
        style={styles.tableRowNumbers}
        rowsPerPageOptions={[5, 10, 25]}
        component="div"
        count={itemData.length}
        rowsPerPage={rowsPerPage}
        page={page}
        onPageChange={handleChangePage}
        onRowsPerPageChange={handleChangeRowsPerPage}
      />
    </Section>
  );
}
